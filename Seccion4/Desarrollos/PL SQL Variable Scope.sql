            V_TEXT VARCHAR2(50) := 'IN-TEXT!';
        BEGIN        
            DBMS_OUTPUT.PUT_LINE('INSIDE-INNER -> ' || V_TEXT);
            DBMS_OUTPUT.PUT_LINE('INSIDE-OUTER -> ' || OUTER.V_TEXT);
        END;
        DBMS_OUTPUT.PUT_LINE('OUTSIDE -> ' || V_TEXT);
    END;
END OUTER;