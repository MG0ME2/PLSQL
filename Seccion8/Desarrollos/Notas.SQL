--PL/SQL CURSORS
	--IMPLICITOS
	SON LOS QUE SE CONSULTA A LA BASE DE DATOS PARA CADA CARGA DE CACHE - POR BASE DE DATOS
	--EXPLICITOS
	SON LOS QUE OBTIENE TODA LA INFORMACION Y GUARDA EN MEMORIA PARA ITERAR MAS RAPIDO
		--PASOS PARA USAR UN CURSOR
		1. SE DECLARA
		2. SE ABRE
		3. SE OBTIENE LA FILA
		4. SE COMPRUEBA QUE SE HA TERMINADO DE ITERAR EL CURSOR
		5. SE CIERRA EL CURSOR
		
		--PASOS
		DECLARE
		OPEN
		FETCH
		CHECK
		CLOSE
	
		--EJEM IN PL/SQL
		DECLARE
			CURSOR [CURSOR_NAME] IS [CONSULTA DE SELECCION];
		BEGIN
			OPEN [CURSOR_NAME];
			FETCH [CURSOR_NAME] INTO [VARIABLES, RECORDs, ETC];
			CLOSE [CURSOR_NAME];
		END;
		
	--CURSORS WITH PARAMETERS
	DECLARE
		CURSOR [CURSOR_NAME([PARAMETER_NAME] DATATYPE, ...)] IS [CONSULTA DE SELECCION WHERE(PARAMETER_NAME)];
	BEGIN
		OPEN [CURSOR_NAME(PARAMETER_VALUE)];
		FETCH [CURSOR_NAME] INTO [VARIABLES, RECORDs, ETC];
		CLOSE [CURSOR_NAME];
	END;
	
	--CURSOR ATTRIBUTES
		%FOUND 		-- RETURNS TRUE IF THE FETCH RETURNED A ROW
		%NOTFOUND	-- OPPOSITE OF %FOUND
		%ISOPEN		-- RETURNS TRUE IF THE CURSOR IS OPEN
		%ROWCOUNT	-- RETURNS THE NUMBER OF FETCHED ROWS
	
	--FOR UPDATE CLAUSE
		FOR UPDATE
		NOWAIT
		WAIT
		FOR UPDATE OF
		
		CURSOR [CURSOR_NAME([PARAMETER_NAME] DATATYPE, ...)]
		IS [CONSULTA DE SELECCION WHERE(PARAMETER_NAME)]
		   FOR UPDATE [OF COLUM(S)] [NOWAIT | WAIT N];
		
	-- REF CURSOR - SYS_REFCURSOR
		TYPE [CURSOR_NAME] IS REF CURSOR [RETURN RETURN_TYPE]
		OPEN [CURSOR_NAME];
		FETCH [CURSOR_NAME];
		CLOSE [CURSOR_NAME];